SCIP_RETCODE GCGcaptureStoredCut(
   GCG_STOREDCUT* storedcut
)
{
   assert(storedcut != NULL);
   assert(storedcut->nuses >= 0);
   (storedcut->nuses)++;
#ifdef SCIP_DEBUG
   SCIP_ROW* row;
   SCIP_CALL( GCGmastercutGetRow(storedcut->mastercutdata, &row) );
   SCIPdebugMessage("capture storedcut: row %s now has c %i nuses\n", SCIProwGetName(row), storedcut->nuses);
#endif

   return SCIP_OKAY;
}

static
SCIP_RETCODE createStoredCut(
   SCIP* scip,
   GCG_STOREDCUT** storedcut,
   GCG_MASTERCUTDATA* mastercutdata
   )
{
   assert(scip != NULL);
   assert(mastercutdata != NULL);
   assert(storedcut != NULL);
   assert(GCGisMaster(scip));

   SCIP_CALL( SCIPallocBlockMemory(scip, storedcut) );
   //SCIPdebugMessage("Size of stored cut: %lu", sizeof(GCG_STOREDCUT));
   (*storedcut)->mastercutdata = mastercutdata;
   (*storedcut)->nuses = 0;
   (*storedcut)->knownvarhistory = NULL; // initially NULL, until row is added to LP for first time
   SCIP_CALL( GCGcaptureStoredCut(*storedcut) );

   // SCIP_CALL( GCGvarhistoryCopyReference(scip, &((*storedcut)->knownvarhistory), GCGgetCurrentVarhistoryReference(scip)) );
   return SCIP_OKAY;
}

static
SCIP_RETCODE freeStoredCut(
   SCIP* scip,
   GCG_STOREDCUT** storedcut
   )
{
   assert(scip != NULL);
   assert(GCGisMaster(scip));
   assert(storedcut != NULL);
   assert(*storedcut != NULL);

#ifdef SCIP_DEBUG
   SCIP_ROW* row;
   SCIP_CALL( GCGmastercutGetRow((*storedcut)->mastercutdata, &row) );
   SCIPdebugMessage("free storedcut: %s row %s will be freed\n", SCIProwIsLocal(row) ? "local": "global", SCIProwGetName(row));
#endif

   if( (*storedcut)->knownvarhistory != NULL )
   {
      SCIPdebugMessage("free storedcut: var history is freed\n");
      SCIP_CALL( GCGvarhistoryFreeReference(scip, &((*storedcut)->knownvarhistory)) );
   }

   GCGmastercutFreeMaster(scip, &((*storedcut)->mastercutdata));
   SCIPfreeBlockMemory(scip, storedcut);

   *storedcut = NULL;

   return SCIP_OKAY;
}

SCIP_RETCODE GCGreleaseStoredCut(
   SCIP* scip,
   GCG_STOREDCUT** storedcut
)
{
   assert(scip != NULL);
   assert(storedcut != NULL);
   assert(*storedcut != NULL);
   assert((*storedcut)->nuses >= 0);

   ((*storedcut)->nuses)--;
#ifdef SCIP_DEBUG
   SCIP_ROW* row;
   SCIP_CALL( GCGmastercutGetRow((*storedcut)->mastercutdata, &row) );
   SCIPdebugMessage("release storedcut: row %s now has r %i nuses\n", SCIProwGetName(row), (*storedcut)->nuses);
#endif
   if( (*storedcut)->nuses == 0 )
   {
      SCIP_CALL( freeStoredCut(scip, storedcut) );
   }

   *storedcut = NULL;

   return SCIP_OKAY;
}

SCIP_RETCODE ensureGeneratedSize(
   SCIP* scip,
   SCIP_EVENTHDLRDATA* eventhdlrdata,
   int sepaidx,
   int size
)
{
   assert(scip != NULL);
   assert(eventhdlrdata != NULL);
   assert(eventhdlrdata->generatedcuts != NULL);
   assert(eventhdlrdata->generatedcuts[sepaidx] != NULL);
   assert(eventhdlrdata->ngeneratecuts[sepaidx] <= eventhdlrdata->generatecutssize[sepaidx]);
   assert(eventhdlrdata->ngeneratecuts[sepaidx] >= 0);

   if( eventhdlrdata->generatecutssize[sepaidx] < size )
   {
      int newmaxcuts = SCIPcalcMemGrowSize(scip, size);
      SCIP_CALL( SCIPreallocBlockMemoryArray(scip, &(eventhdlrdata->generatedcuts[sepaidx]), eventhdlrdata->generatecutssize[sepaidx], newmaxcuts) );
      eventhdlrdata->generatecutssize[sepaidx] = newmaxcuts;
   }

   assert(eventhdlrdata->generatecutssize[sepaidx] >= size);

   return SCIP_OKAY;
}


