SCIP_RETCODE GCGaddCutToGeneratedCutsSepa(
   SCIP* scip,
   GCG_MASTERCUTDATA* mastercutdata,
   int sepaidx
   )
{
   SCIP_EVENTHDLR* eventhdlr;
   SCIP_EVENTHDLRDATA* eventhdlrdata;
   GCG_STOREDCUT* storedcut;
   SCIP_ROW* row;

   assert(scip != NULL);
   assert(GCGisMaster(scip));
   assert(mastercutdata != NULL);

   eventhdlr = SCIPfindEventhdlr(scip, EVENTHDLR_NAME);
   assert(eventhdlr != NULL);

   eventhdlrdata = SCIPeventhdlrGetData(eventhdlr);
   assert(eventhdlrdata != NULL);

   SCIP_CALL( GCGmastercutGetRow(mastercutdata, &row) );
   assert(row != NULL);

   SCIP_CALL( ensureGeneratedSize(scip, eventhdlrdata, sepaidx, eventhdlrdata->ngeneratecuts[sepaidx] + 1) );
   SCIP_CALL( createStoredCut(scip, &storedcut, mastercutdata) );
   eventhdlrdata->generatedcuts[sepaidx][eventhdlrdata->ngeneratecuts[sepaidx]] = storedcut;
   SCIP_CALL( SCIPhashmapSetImageInt(eventhdlrdata->rowxgeneratedidx, row, eventhdlrdata->ngeneratecuts[sepaidx]) );
   (eventhdlrdata->ngeneratecuts[sepaidx])++;

   return SCIP_OKAY;
}

SCIP_RETCODE GCGreaddGlobalCut(
   SCIP* scip,
   GCG_STOREDCUT* storedcut,
   SCIP_EVENTHDLRDATA* eventhdlrdata,
   int sepaidx
)
{
   SCIP_ROW* row;

   assert(scip != NULL);
   assert(storedcut != NULL);
   assert(eventhdlrdata != NULL);

   SCIP_CALL( GCGmastercutGetRow(storedcut->mastercutdata, &row) );
   SCIPdebugMessage("re-add global cuts: row %s is added to generated guts\n", SCIProwGetName(row));
   SCIP_CALL( ensureGeneratedSize(scip, eventhdlrdata, sepaidx, eventhdlrdata->ngeneratecuts[sepaidx] + 1) );
   eventhdlrdata->generatedcuts[sepaidx][eventhdlrdata->ngeneratecuts[sepaidx]] = storedcut;
   SCIP_CALL( GCGcaptureStoredCut(eventhdlrdata->generatedcuts[sepaidx][eventhdlrdata->ngeneratecuts[sepaidx]]) );
   SCIP_CALL( SCIPhashmapSetImageInt(eventhdlrdata->rowxgeneratedidx, row, eventhdlrdata->ngeneratecuts[sepaidx]) );
   (eventhdlrdata->ngeneratecuts[sepaidx])++;

   return SCIP_OKAY;
}


SCIP_RETCODE GCGclearGeneratedCuts(
   SCIP* scip
   )
{
   SCIP_EVENTHDLR* eventhdlr;
   SCIP_EVENTHDLRDATA* eventhdlrdata;
   int i;
   int j;

   assert(scip != NULL);
   assert(GCGisMaster(scip));

   eventhdlr = SCIPfindEventhdlr(scip, EVENTHDLR_NAME);
   assert(eventhdlr != NULL);

   eventhdlrdata = SCIPeventhdlrGetData(eventhdlr);
   assert(eventhdlrdata != NULL);

   SCIPdebugMessage("clear generated cuts: \n");
   /* clear out generatedcuts array */
   for( i = 0; i < eventhdlrdata->nsepas; i++ )
   {
      SCIPdebugMessage("clear generated cuts: sepa %i has %i cuts\n", i, eventhdlrdata->ngeneratecuts[i]);
      for( j = 0; j < eventhdlrdata->ngeneratecuts[i]; j++ )
      {
         SCIP_CALL( GCGreleaseStoredCut(scip, &(eventhdlrdata->generatedcuts[i][j])) );
      }
      eventhdlrdata->ngeneratecuts[i] = 0;
   }

   /* clear corresponding map */
   SCIPhashmapRemoveAll(eventhdlrdata->rowxgeneratedidx);

   return SCIP_OKAY;
}

SCIP_RETCODE GCGremoveNewInactiveRows(
   SCIP* scip,
   int* startidx
   )
{
   SCIP_EVENTHDLR* eventhdlr;
   SCIP_EVENTHDLRDATA* eventhdlrdata;
   SCIP_ROW* row;
   int i;
   int j;

   assert(scip != NULL);
   assert(GCGisMaster(scip));

   eventhdlr = SCIPfindEventhdlr(scip, EVENTHDLR_NAME);
   assert(eventhdlr != NULL);

   eventhdlrdata = SCIPeventhdlrGetData(eventhdlr);
   assert(eventhdlrdata != NULL);

   for( i = 0; i < eventhdlrdata->nsepas; i++ )
   {
      for( j = eventhdlrdata->nactivecuts[i] - 1; j >= startidx[i]; --j )
      {
         SCIP_CALL( GCGmastercutGetRow(eventhdlrdata->activecuts[i][j]->mastercutdata, &row) );
         /* rows still in LP remain relevant */
         if( SCIProwIsInLP(row) )
            continue;

         /* cut was created and removed at focus node: can be permanently removed */
         SCIPdebugMessage("remove new inactive rows: remove row %s and free data\n", SCIProwGetName(row));
         SCIP_CALL( GCGreleaseStoredCut(scip, &(eventhdlrdata->activecuts[i][j])) );
         assert(eventhdlrdata->activecuts[i][j] == NULL);
         (eventhdlrdata->nactivecuts[i])--;
         eventhdlrdata->activecuts[i][j] = eventhdlrdata->activecuts[i][eventhdlrdata->nactivecuts[i]];
      }
   }
   return SCIP_OKAY;
}

SCIP_RETCODE GCGshrinkActiveCuts(
   SCIP* scip,
   int* newnrows
   )
{
   SCIP_EVENTHDLR* eventhdlr;
   SCIP_EVENTHDLRDATA* eventhdlrdata;
   SCIP_ROW* row;
   int i;
   int j;

   assert(scip != NULL);
   assert(GCGisMaster(scip));

   eventhdlr = SCIPfindEventhdlr(scip, EVENTHDLR_NAME);
   assert(eventhdlr != NULL);

   eventhdlrdata = SCIPeventhdlrGetData(eventhdlr);
   assert(eventhdlrdata != NULL);
   assert(eventhdlrdata->nsepas > 0);

   for( i = 0; i < eventhdlrdata->nsepas; i++ )
   {
      assert(eventhdlrdata->nactivecuts[i] >= newnrows[i]);
      for( j = eventhdlrdata->nactivecuts[i] - 1; j >= newnrows[i]; --j )
      {
         SCIP_CALL( GCGmastercutGetRow(eventhdlrdata->activecuts[i][j]->mastercutdata, &row) );

         /* global cuts with age zero, are added to the separation storage before initial LP is constructed
          * --> these cuts will be re-added to the next LP */
         if( scip->tree->correctlpdepth != -1 && !SCIProwIsLocal(row) && SCIProwGetAge(row) == 0 )
         {
            SCIP_CALL( GCGreaddGlobalCut(scip, eventhdlrdata->activecuts[i][j], eventhdlrdata, i) );
         }

         SCIP_CALL( GCGreleaseStoredCut(scip, &(eventhdlrdata->activecuts[i][j])) );
      }
      eventhdlrdata->nactivecuts[i] = newnrows[i];
   }

   return SCIP_OKAY;
}

